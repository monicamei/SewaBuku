    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int ok= JOptionPane.showConfirmDialog(null, "Anda Yakin Ingin Menghapus Data Yang Dipilih ? ="+lm.getText()+"'",".::Konfirmasi::.",JOptionPane.YES_NO_OPTION);
        // TODO add your handling code here:
        if(ok==0){
            try{
                java.sql.Connection conn=(Connection)
                koneksiDB1.configDB();
                String sql = "Delete from denda where "+ "nm_peminjam='"+nm.getText() +"'";
                java.sql.PreparedStatement
                pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Berhasil di Hapus");
                nm.setText("");
                lm.setText("");
                hrg.setText("");
                byr.setText("");
                kmbl.setText("");
                nm.requestFocusInWindow();
                buka_tabel();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Proses Hapus Gagal");
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

